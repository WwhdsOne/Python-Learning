# 2.while循环用于在给定的判断条件为真的情况下重复执行一段程序，即只要条件满足，就不断循环，条件不满足则退出循环。
# 基本的while循环
i = 0
while i < 5:
    print(i)
    i += 1  # 注意更新循环变量，否则可能会导致无限循环 
# 在循环中，你还可以使用break来退出整个循环，或者使用continue来跳过当前循环的剩余部分并进入下一次循环。

'''
练习案例-打印九九乘法表
通过while循环,输出九九乘法表内容
提示：
· 2层循环,外层控制行,内层控制列
· 外层循环和内存循环的累加数字变量,用以辅助输出乘法表的数值
'''

for i in range(10):
    if i == 5:
        break
    print(i)
# 输出：0 1 2 3 4

for i in range(10):
    if i == 5:
        continue
    print(i)
# 输出：0 1 2 3 4 6 7 8 9