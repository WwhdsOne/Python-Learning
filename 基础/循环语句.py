# 在Python中，我们主要使用for和while两种循环语句。

# 1.for循环：for循环用于遍历任何序列（如列表或字符串）或其他可迭代对象。
# 遍历列表
for i in [1, 2, 3, 4, 5]:
    print(i)

# 遍历字符串
for char in "Hello":
    print(char)

# 使用range函数生成序列
for i in range(5):  # 生成0到4的序列
    print(i)

