# Python的列表（List）是一种有序的集合,可以随时添加和删除其中的元素。

# 1.创建列表:使用方括号[]创建列表,元素之间用逗号,分隔。
my_list = [1, 2, 3, 'a', 'b', 'c']

# 2.访问元素:通过索引来访问列表中的元素,索引从0开始。
print(my_list[0])  # 输出:1

# 3.修改元素:通过索引来修改列表中的元素。
my_list[0] = 'one'
print(my_list)  # 输出:['one', 2, 3, 'a', 'b', 'c']

# 4.添加元素:使用append()方法在列表末尾添加元素。
my_list.append('d')
print(my_list)  # 输出:['one', 2, 3, 'a', 'b', 'c', 'd']

# 5.删除元素:使用del语句或remove()方法删除元素。
del my_list[0]
print(my_list)  # 输出:[2, 3, 'a', 'b', 'c', 'd']

my_list.remove('d')
print(my_list)  # 输出:[2, 3, 'a', 'b', 'c']

# 6.列表长度:使用len()函数获取列表的长度。
print(len(my_list))  # 输出:5

# 以上就是Python列表的基础用法,包括如何创建列表、访问元素、修改元素、添加元素、删除元素和获取列表长度。

